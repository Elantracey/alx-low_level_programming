/**          
 * isupper - function that checks for upper case character
 * @c: input character
 * Return: 1, if uppercase, 0 if not
 */
int _isupper(int c)
{
	if (c >= "A" && c <= "Z")
	{
		Return: (1);
	}
	else
	{
		Return: (0);
	}
}
